// Pętla for

var x = 5;
var y = 10;
var result = 0;

for (i = x; i <= y; i++) {
	result = result + i;
	console.log(i);
}
console.log(result);


// Pętla while

var x = 5;
var y = 10;
var result = 0;

while (x <= y) {
	result += x;
	x++;
}
console.log(result);